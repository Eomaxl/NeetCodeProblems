package org.greedy;

public class MaximumSubarray {
    public int maxSubArray(int[] nums){
        int maxSum = nums[0], currSum = nums[0];
        for(int num: nums){
            if(currSum <0){
                currSum = 0;
            }
            currSum += num;
            maxSum = Math.max(maxSum, currSum);
        }
        return maxSum;
    }
}

// TC: O(n), SC: O(1)
